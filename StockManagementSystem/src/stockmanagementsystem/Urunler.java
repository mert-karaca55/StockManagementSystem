    public void table_load(){
        try {
            myStat=(PreparedStatement) myConn.prepareStatement("select * from urunlerr");
            ResultSet rs=myStat.executeQuery();
            
            ResultSetMetaData rd= rs.getMetaData();
            int c;
            c=rd.getColumnCount();
            df=(DefaultTableModel)jTable1.getModel();
            df.setRowCount(0);
            
            while(rs.next()){
                Vector v2 = new Vector();
                for(int i=1;i<=c;i++){
                    v2.add(rs.getString("urun_id"));
                    v2.add(rs.getString("urun_adi"));
                    v2.add(rs.getString("urun_fiyati"));
                    
                    v2.add(rs.getString("tedarik_id"));
                    v2.add(rs.getString("depo_id"));
                    
                    
                }
                df.addRow(v2);
            }
            
            
        } catch (SQLException ex) {
            Logger.getLogger(Urunler.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    public void table_load(){
        try {
            myStat=(PreparedStatement) myConn.prepareStatement("select * from musteri");
            ResultSet rs=myStat.executeQuery();
            
            ResultSetMetaData rd= rs.getMetaData();
            int c;
            c=rd.getColumnCount();
            df=(DefaultTableModel)jTable1.getModel();
            df.setRowCount(0);
            
            while(rs.next()){
                Vector v2 = new Vector();
                for(int i=1;i<=c;i++){
                    v2.add(rs.getString("musteri_id"));
                    v2.add(rs.getString("musteri_ad"));
                    v2.add(rs.getString("musteri_telefon"));
                    v2.add(rs.getString("musteri_adres"));
      
                }
                df.addRow(v2);
            }
            
            
        } catch (SQLException ex) {
            Logger.getLogger(Urunler.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

